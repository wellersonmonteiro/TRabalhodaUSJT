    private void jbCriararquivoActionPerformed(java.awt.event.ActionEvent evt) {
        if (caminhoArquivo.contains("pdf")) {
            try {
                Connection con = Conexao.fazConexao();
                String sql = "INSERT INTO `db_leafless`.`tb_documentos` (`id_autor`,`titulo`, `data_inclusao`, `caminho`, `extensao`, `versao`, `temporalidade`, `permissoes`, `nome_autor`)\n"
                        + "VALUES (1,?, NOW(), ?, ?, ?, NULL, ?, ?);";
                PreparedStatement ps = con.prepareStatement(sql);

                // Obtenção dos valores dos campos
                String titulo = jTextField1.getText(); // Obtém o valor do campo de texto
                String caminho = caminhoArquivo; // Define o caminho do arquivo (deve ser ajustado)
                String extensao = "pdf"; // Define a extensão do arquivo (deve ser ajustado)
                String versao = "1.0"; // Define a versão do documento (deve ser ajustado)
                String permissoes = "privado"; // Define as permissões do documento (deve ser ajustado)
                String nomeAutor = TelaLoggin.naofazer; // Define o nome do autor do documento (deve ser ajustado)

                // Define os valores nos parâmetros do PreparedStatement
                ps.setString(1, titulo);
                ps.setString(2, caminho);
                ps.setString(3, extensao);
                ps.setString(4, versao);
                ps.setString(5, permissoes);
                ps.setString(6, nomeAutor);

                // Executa o comando SQL
                int rowsAffected = ps.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Arquivo adicionado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro!");
                }

                con.close(); // Fecha a conexão com o banco de dados
            } catch (SQLException ex) {
                Logger.getLogger(InserirArquivo.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Formato incorreto!!");
        }
    }

