    private void jbobteraquivoActionPerformed(java.awt.event.ActionEvent evt) {
        JFileChooser fileChooser = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());

        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            java.io.File arquivoSelecionado = fileChooser.getSelectedFile();
            caminhoArquivo = arquivoSelecionado.getAbsolutePath();

            System.out.println("Caminho do arquivo selecionado: " + caminhoArquivo);
        } else {
            System.out.println("Nenhum arquivo selecionado.");
        }
    }

